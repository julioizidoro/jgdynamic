    public void carregarVendedor(String nome){
        VendedorController vendedorController = new VendedorController();
        listaVendedor = vendedorController.consultarVendedor(nome, config.getEmpresa().getIdempresa());
        if (listaVendedor==null){
            listaVendedor = new ArrayList<Vendedor>();
        }
        model = new ConsultaVendedorTableModel(listaVendedor);
        vendedorjTable.setModel(model);
        vendedorjTable.getColumnModel().getColumn(0).setPreferredWidth(200);
        vendedorjTable.getColumnModel().getColumn(1).setPreferredWidth(100);
        vendedorjTable.repaint();
    }
