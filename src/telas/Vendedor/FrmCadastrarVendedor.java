    private void inicializar(){
        if (vendedor==null){
            vendedor = new Vendedor();
        }else {
            nomeCompletojTextField.setText(vendedor.getNomeCompleto());
            situacaojComboBox.setSelectedItem(vendedor.getSituacao());
            nomejTextField.setText(vendedor.getNome());
            vistajTextField.setText(Formatacao.foramtarDoubleString(vendedor.getPercVista()));
            prazojTextField.setText(Formatacao.foramtarDoubleString(vendedor.getPercPrazo()));
        }
        nomeCompletojTextField.requestFocus();
    }

