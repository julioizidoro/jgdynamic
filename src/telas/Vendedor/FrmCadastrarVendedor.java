    private void salvarjButton2SalvarCadastroAssociado(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButton2SalvarCadastroAssociado
        vendedor.setNomeCompleto(nomeCompletojTextField.getText());
        vendedor.setSituacao(situacaojComboBox.getSelectedItem().toString());
        vendedor.setNome(nomejTextField.getText());
        vendedor.setPercVista(Formatacao.formatarStringDouble(vistajTextField.getText()));
        vendedor.setPercPrazo(Formatacao.formatarStringDouble(prazojTextField.getText()));
        vendedor.setEmpresa(config.getEmpresa().getIdempresa());
        VendedorController vendedorController = new VendedorController();
        vendedorController.salvar(vendedor);
        this.telaVendedor.carregarVendedor("");
        this.dispose();
    }//GEN-LAST:event_salvarjButton2SalvarCadastroAssociado

