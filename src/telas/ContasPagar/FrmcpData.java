    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (tipo==3){
            telaContas.setRelContasPagarPeriodo(dataIniciojDateChooser.getDate(), dataFinaljDateChooser.getDate());
        }else if (tipo==2){
            telaContas.setRelContasPagarFornecedor(this.fornecedor.getIdfornecedor(), this.fornecedor.getRazaoSocial());
        }else if (tipo==5){
            telaContas.setRelContasVencidasFornecedor(dataIniciojDateChooser.getDate(), dataFinaljDateChooser.getDate(), 
                    this.fornecedor.getIdfornecedor(), this.fornecedor.getRazaoSocial());
        }else if (tipo==6){
            telaContas.setRelContasPagasPeriodo(dataIniciojDateChooser.getDate(), dataFinaljDateChooser.getDate());
        }else if (tipo==7){
            telaContas.setRelContasPagasFornecedor(this.fornecedor.getIdfornecedor(), this.fornecedor.getRazaoSocial(), 
                    dataIniciojDateChooser.getDate(), dataFinaljDateChooser.getDate());
        }else if (tipo==8){
            telaContas.setRelContasPagasPlanoContas(this.planoContas.getIdplanoconta(), this.planoContas.getDescricao(), 
                    dataIniciojDateChooser.getDate(), dataFinaljDateChooser.getDate());
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

