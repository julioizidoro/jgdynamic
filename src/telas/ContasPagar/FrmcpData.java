    public void consultarFornecedor(Fornecedor fornecedor){
        this.fornecedor = fornecedor;
        codigoFornecedorjTextField.setText(String.valueOf(this.fornecedor.getIdfornecedor()));
        razaoSocialjTextField.setText(this.fornecedor.getRazaoSocial());
    }
    
    public void cosultarPlanoContas(Planoconta planoContas){
        this.planoContas = planoContas;
        codigoContajTextField.setText(this.planoContas.getNumeroConta());
        descricaojTextField.setText(planoContas.getDescricao());
    }
    
    public void ativarCampos(){
        if (tipo==2){
            dataFinaljDateChooser.setEnabled(false);
            dataIniciojDateChooser.setEnabled(false);
            codigoContajTextField.setEnabled(false);
            consultaPlanoContasjButton.setEnabled(false);
        }else if (tipo==3){
            codigoContajTextField.setEditable(false);
            consultaPlanoContasjButton.setEnabled(false);
            codigoFornecedorjTextField.setEditable(false);
            razaoSocialjTextField.setEditable(false);
        }else if (tipo==5){
            dataFinaljDateChooser.setEnabled(false);
            dataIniciojDateChooser.setEnabled(false);
            codigoContajTextField.setEditable(false);
            consultaPlanoContasjButton.setEnabled(false);
        }else if (tipo==6){
            codigoFornecedorjTextField.setEditable(false);
            razaoSocialjTextField.setEditable(false);
            codigoContajTextField.setEditable(false);
            consultaPlanoContasjButton.setEnabled(false);
        }else if (tipo==7){
            codigoContajTextField.setEditable(false);
            consultaPlanoContasjButton.setEnabled(false);
        }else if (tipo==8){
            dataFinaljDateChooser.setEnabled(false);
            dataIniciojDateChooser.setEnabled(false);
            codigoFornecedorjTextField.setEditable(false);
            razaoSocialjTextField.setEditable(false);
        }
    }

