      public void listarContasMes() {
        boolean achouMes = false;
        listaMes = new ArrayList<ContaMes>();
        String sql = "select c from Contaspagar c where c.pagamentocontaspagar=1 order by c.dataVencimento";
        ContasPagarController contasPagarController = new ContasPagarController();
        List<Contaspagar> listaContaPagar = contasPagarController.listarContas(sql);
        if (listaContaPagar != null) {
            for (int i = 0; i < listaContaPagar.size(); i++) {
                for (int l = 0; l < listaMes.size(); l++) {
                    if (listaContaPagar.get(i).getMes().equalsIgnoreCase(listaMes.get(l).getMes())) {
                        achouMes = true;
                        listaMes.get(l).setValor(listaContaPagar.get(i).getValorConta() + listaMes.get(l).getValor());
                        l = listaMes.size() + 10;
                    }
                }
                if (!achouMes) {
                    ContaMes contaMes = new ContaMes();
                    contaMes.setMes(listaContaPagar.get(i).getMes());
                    contaMes.setValor(listaContaPagar.get(i).getValorConta());
                    listaMes.add(contaMes);
                }
                achouMes = false;
            }
        }
          System.out.println("1");

    }
      
    public void carregarTableModel(){
        if (listaMes!=null){
            modelMes = new ContaMesTableModel(listaMes);
        }else modelMes = new ContaMesTableModel(new ArrayList<ContaMes>());
        DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
        renderer.setHorizontalAlignment(SwingConstants.RIGHT);
        mesjTable.setModel(modelMes);
        mesjTable.getColumnModel().getColumn(0).setPreferredWidth(30);
        mesjTable.getColumnModel().getColumn(1).setCellRenderer(renderer);
        mesjTable.getColumnModel().getColumn(1).setPreferredWidth(40);
        mesjTable.repaint();
    }
    
    public void calcularValorTotal(){
        double valorTotal =0;
        if (listaMes!=null){
            for(int i=0;i<listaMes.size();i++){
                valorTotal = valorTotal + listaMes.get(i).getValor();
            }
        }
        valorTotaljLabel.setText(Formatacao.foramtarDoubleString(valorTotal));
    }
