    private void confirmajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmajButtonActionPerformed
        if (docEntrada.getIdDocEntrada()==null){
            docEntrada.setDataEntrada(dataEntradajDateChooser.getDate());
        }
        docEntrada.setCondicaoPagamento(condicaoPagamentojComboBox.getSelectedItem().toString());
        docEntrada.setEmpresa(config.getEmpresa().getIdempresa());
        docEntrada.setFornecedor(fornecedor.getIdfornecedor());
        docEntrada.setSituacao(situacaojComboBox.getSelectedItem().toString());
        docEntrada.setTipoDocumento(tipoDocumentojComboBox.getSelectedItem().toString());
        if (valorDescontojTextField.getText().length()>0){
            docEntrada.setValorDesconto(Formatacao.formatarStringfloat(valorDescontojTextField.getText()));
        }else docEntrada.setValorDesconto(0.0f);
        if (valorEncargosjTextField.getText().length()>0){
            docEntrada.setValorEncargos(Formatacao.formatarStringfloat(valorEncargosjTextField.getText()));
        }else docEntrada.setValorEncargos(0.0f);
        if (valorTotaljTextField.getText().length()>0){
            docEntrada.setValorTotal(Formatacao.formatarStringfloat(valorTotaljTextField.getText()));
        }else docEntrada.setValorTotal(0.0f);
        EntradaProdutoController entradaProdutoController = new EntradaProdutoController();
        docEntrada = entradaProdutoController.salvarDocEntrada(docEntrada);
        if (notaFiscal == null) {
            notaFiscal = new Nfentrada();
            if (docEntrada.getTipoDocumento().equalsIgnoreCase("Nota Fiscal")) {
                notaFiscal.setArquivonfe(xmlNfe);
                notaFiscal.setDataEmissao(dataEmissaojDateChooser.getDate());
                notaFiscal.setDataSaida(dataSaidajDateChooser.getDate());
                notaFiscal.setEspecie(especiejComboBox.getSelectedItem().toString());
                notaFiscal.setNumero(numerojTextField.getText());
                notaFiscal.setSerie(seriejTextField.getText());
                notaFiscal.setSubSerie(subSeriejTextField.getText());
            }
            notaFiscal.setTipoDocumento(tipoDocumentojComboBox.getSelectedItem().toString());
            if (valorProdutosjTextField.getText().length() > 0) {
                notaFiscal.setValorProdutos(Formatacao.formatarStringfloat(valorProdutosjTextField.getText()));
            } else {
                notaFiscal.setValorProdutos(0.0f);
            }
            notaFiscal.setValorTotal(docEntrada.getValorTotal());
            notaFiscal.setDocentrada(docEntrada.getIdDocEntrada());
            notaFiscal = entradaProdutoController.salvarEntradaNF(notaFiscal);
        }
        produtosjButton.setEnabled(true);
        confirmajButton.setEnabled(false);
        new FrmNovaEntrada(config, docEntrada, notaFiscal, listaDuplicata, listaProdutos, nota);
        
    }//GEN-LAST:event_confirmajButtonActionPerformed

