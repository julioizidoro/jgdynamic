    public void setArquivo(File arquivo) {
        if (arquivo!=null){
            caminhojTextField.setText(arquivo.getPath());
            try {
                gerarArquivoBanco(arquivo);
            } catch (IOException ex) {
                Logger.getLogger(FrmNovaEntrada.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
    public void gerarArquivoBanco(File arquivo) throws IOException {
        File file = arquivo;
        InputStream is = new FileInputStream(file);
        xmlNfe = new byte[(int) file.length()];
        int offset = 0;
        int numRead = 0;
        while (offset < xmlNfe.length
                && (numRead = is.read(xmlNfe, offset, xmlNfe.length - offset)) >= 0) {
            offset += numRead;
        }
    }
    
    public void carregarDadosDocEntrada(){
        FornecedorController fornecedorController = new FornecedorController();
        fornecedor = fornecedorController.consultarFornecedor(docEntrada.getFornecedor());
        fornecedorjTextField.setText(fornecedor.getRazaoSocial());
        dataEntradajDateChooser.setDate(docEntrada.getDataEntrada());
        tipoDocumentojComboBox.setSelectedItem(docEntrada.getTipoDocumento());
        condicaoPagamentojComboBox.setSelectedItem(docEntrada.getCondicaoPagamento());
        valorEncargosjTextField.setText(Formatacao.foramtarFloatString(docEntrada.getValorEncargos()));
        valorDescontojTextField.setText(Formatacao.foramtarFloatString(docEntrada.getValorDesconto()));
        valorProdutosjTextField.setText(Formatacao.foramtarFloatString(docEntrada.getValorTotal()));
        situacaojComboBox.setSelectedItem(docEntrada.getSituacao());
        EntradaProdutoController entradaProdutoController = new EntradaProdutoController();
        notaFiscal = entradaProdutoController.consultarNotaFiscalEntrada(docEntrada.getIdDocEntrada());
        //Preencher Dados Nota Fiscal
        if (notaFiscal != null) {
            numerojTextField.setText(notaFiscal.getNumero());
            dataEmissaojDateChooser.setDate(notaFiscal.getDataEmissao());
            dataSaidajDateChooser.setDate(notaFiscal.getDataSaida());
            especiejComboBox.setSelectedItem(notaFiscal.getEspecie());
            seriejTextField.setText(notaFiscal.getSerie());
            subSeriejTextField.setText(notaFiscal.getSubSerie());
            valorProdutosjTextField.setText(Formatacao.foramtarFloatString(notaFiscal.getValorProdutos()));
            valorTotaljTextField.setText(Formatacao.foramtarFloatString(notaFiscal.getValorTotal()));
            xmlNfe = notaFiscal.getArquivonfe();
        }
    }
    
    public void setCamposNFe() {

        //Preenche os dados da Entrada


        docEntrada.setFornecedor(fornecedor.getIdfornecedor());
        docEntrada.setCondicaoPagamento(nota.getFormaPagamento());
        docEntrada.setDataEntrada(new Date());
        docEntrada.setTipoDocumento("Nota Fiscal");
        docEntrada.setValorDesconto(nota.getValorDesconto());
        docEntrada.setValorEncargos(nota.getValorOutrasDespesass());
        docEntrada.setEmpresa(config.getEmpresa().getIdempresa());
        fornecedorjTextField.setText(fornecedor.getRazaoSocial());
        tipoDocumentojComboBox.setSelectedItem("Nota Fiscal");
        condicaoPagamentojComboBox.setSelectedItem(this.nota.getFormaPagamento());
        valorEncargosjTextField.setText(Formatacao.foramtarFloatString(this.nota.getValorOutrasDespesass() + this.nota.getValorFrete()));
        valorDescontojTextField.setText(Formatacao.foramtarFloatString(this.nota.getValorDesconto()));
        valorProdutosjTextField.setText(Formatacao.foramtarFloatString(this.nota.getValorProdutos()));
        valorTotaljTextField.setText(Formatacao.foramtarFloatString(this.nota.getValorTotal()));

        //Preenche os dados da Nota Fiscal

        numerojTextField.setText(this.nota.getNumero());
        dataEmissaojDateChooser.setDate(this.nota.getDataEmissao());
        if (this.nota.getDataSaida()==null){
            dataSaidajDateChooser.setDate(this.nota.getDataEmissao());
        }else dataSaidajDateChooser.setDate(this.nota.getDataSaida());
        especiejComboBox.setSelectedItem("NF-e");
        seriejTextField.setText(this.nota.getSerie());
        subSeriejTextField.setText(this.nota.getSubSerie());
        if (this.docEntrada.getFornecedor()==0){
            new FrmCadastrarFornecedor(fornecedor, this);
        }


    }


