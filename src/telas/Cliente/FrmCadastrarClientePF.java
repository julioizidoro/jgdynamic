    public void popularTelaFechamento(){
        tipoFechamentojComboBox.setSelectedItem(clientefechamento.getTipoFechamento());
        String diaPagto = String.valueOf(clientefechamento.getDiaPagamento());
        if (clientefechamento.getDiaPagamento()<10){
            diaPagto = "0" + diaPagto;
        }
        diaPagamentojComboBox.setSelectedItem(diaPagto);
        formaPagamentojComboBox.setSelectedItem(clientefechamento.getFormaPagamento());
        emitirComprovantejComboBox.setSelectedItem(clientefechamento.getNotaFiscal());
        limiteCreditojTextField.setText(Formatacao.foramtarDoubleString(clientefechamento.getLimiteCredito()));
        clienteDescontojComboBox.setSelectedItem(cliente.getPossuiDesconto());
    }
    
    public void popularTelaEndereco(){
        String tipoLogradouro = clienteendereco.getTipoLogradouro();
        tipoLogradourojComboBox.setSelectedItem(tipoLogradouro);
        logradourojTextField.setText(clienteendereco.getLogradouro());
        numerojTextField.setText(clienteendereco.getNumero());
        complementojTextField.setText(clienteendereco.getComplemento());
        bairrojTextField.setText(clienteendereco.getBairro());
        cepjFormattedTextField.setText(clienteendereco.getCep());
        if (clienteendereco.getMunicipios() == 0) {
            cidadejTextField.setText(clienteendereco.getCidade());
            estadojTextField.setText(clienteendereco.getEstado());
        } else {
            MunicipiosController municipiosController = new MunicipiosController();
            this.municipios = municipiosController.getMunicipio(clienteendereco.getMunicipios());
            if (municipios != null) {
                cidadejTextField.setText(municipios.getNomeMunicipio());
                estadojTextField.setText(municipios.getEstado());
            }
        }
    }
    
