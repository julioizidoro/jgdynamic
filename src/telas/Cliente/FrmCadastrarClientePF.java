    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        cliente.setDataAtualizacao(dataAtualizacaojDateChooser.getDate());
        String data= "11/11/1111";
        if (cliente.getDataAtualizacao()!=null){
            data = Formatacao.ConvercaoDataSql(cliente.getDataAtualizacao());
        }
        if (data.equalsIgnoreCase("1900/01/01")) {
            salvarDados();
            telaConsulta.carregarTodosClientes();
            this.dispose();
        } else {
            String validar = validarTelaDadosIdentificacao();
            if (validar != null) {
                JOptionPane.showMessageDialog(rootPane, validar);
            } else {
                validar = validarDadosComplementares();
                if (validar != null) {
                    JOptionPane.showMessageDialog(rootPane, validar);
                } else {
                    salvarDados();
                    telaConsulta.carregarTodosClientes();
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_salvarjButtonActionPerformed

