    public void preencherCampos(){
        popularObjetos();
        if (this.clienteendereco!=null){
            popularTelaEndereco();
        }else {
            this.clienteendereco = new Clienteendereco();
        }
        if (this.clientefechamento!=null){
            popularTelaFechamento();
        }else {
            this.clientefechamento = new Clientefechamento();
        }
        if (this.clientepessoafisica!=null){
            popularTelaDadosComplementares();
            popularTelaDadosComercial();
        }else {
            this.clientepessoafisica = new Clientepessoafisica();
        }
        if (cliente!=null){
            popularTelaDadosIdentificacao();
        }else {
            this.cliente = new Cliente();
        }
    }
    
