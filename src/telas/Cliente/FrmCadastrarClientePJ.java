    public void salvarDados(){
        popularCliente();
        ClienteController clienteController = new ClienteController();
        cliente = clienteController.salvarCliente(cliente);
        
        popularEndereco();
        clienteendereco = clienteController.salvarEndereco(clienteendereco);
        
        popularFechamento();
        clientefechamento = clienteController.salvarFechamento(clientefechamento);
        
        if (listaSocios != null) {
            for (int i = 0; i < listaSocios.size(); i++) {
                listaSocios.get(i).setClienteIdcliente(this.cliente.getIdcliente());
                clienteController.salvarSocios(listaSocios.get(i));

            }
        }
        
        listaSocios = clienteController.listarClienteSocios(cliente.getIdcliente());
        clienteSocio = localizarSocioResponsavel("Sócio Administrador");
        if (clientepessoajuridica==null){
            clientepessoajuridica = new Clientepessoajuridica();
        }
        if (clienteSocio != null) {
            if (clienteSocio.getIdclienteSocios() > 0) {
                clientepessoajuridica.setSocioResponsavel(clienteSocio.getIdclienteSocios());
            } else {
                clientepessoajuridica.setSocioResponsavel(0);
            }
        }
        
        popularPessoaJuridica();
        clientepessoajuridica= clienteController.salvarPessoaJuridica(clientepessoajuridica);
        
    }
    
    
