    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean val_cpf = Formatacao.validaCPF(cpfSociojFormattedTextField.getText());
        if (val_cpf) {
            Clientesocios cliSocio = new Clientesocios();
            cliSocio.setNome(nomeSociojTextField.getText());
            cliSocio.setCpf(cpfSociojFormattedTextField.getText());
            cliSocio.setTipoSocio(tipoSociojComboBox.getSelectedItem().toString());
            if (listaSocios == null) {
                listaSocios = new ArrayList<Clientesocios>();
            }
            listaSocios.add(cliSocio);
            setModelSocios();
            nomeSociojTextField.setText("");
            cpfSociojFormattedTextField.setText("");
            try {
                cpfSociojFormattedTextField.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("###.###.###-##")));
            } catch (ParseException ex) {
                Logger.getLogger(FrmCadastrarClientePJ.class.getName()).log(Level.SEVERE, null, ex);
            }
            nomeSociojTextField.requestFocus();
        } else {
            JOptionPane.showMessageDialog(null, "CPF invalido");
            cpfSociojFormattedTextField.requestFocus();
            cpfSociojFormattedTextField.selectAll();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

