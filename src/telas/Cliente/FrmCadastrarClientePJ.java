    public Clientesocios localizarSocioResponsavel(){
        if (listaSocios.size()>0){
            for (int i=0;i<listaSocios.size();i++){
                if (listaSocios.get(i).getIdclienteSocios()==clientepessoajuridica.getSocioResponsavel()){
                    return listaSocios.get(i);
                } 
            }
        }else return null;
        return null;
    }
    
    public Clientesocios localizarSocioResponsavel(String tipo) {
        if (listaSocios != null) {
            if (listaSocios.size() > 0) {
                for (int i = 0; i < listaSocios.size(); i++) {
                    if (listaSocios.get(i).getTipoSocio().equalsIgnoreCase(tipo)) {
                        return listaSocios.get(i);
                    }
                }
            } else {
                return null;
            }
        }
        return null;
    }
            
    
