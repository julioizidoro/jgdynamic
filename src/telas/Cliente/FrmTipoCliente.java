    private void salvarjButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButton2ActionPerformed
        if (pfcontajRadioButton.isSelected()){
            new FrmCadastrarClientePF(null, telaConsulta, 0);
            this.dispose();
        }else if (pjcontajRadioButton.isSelected()){
            new FrmCadastrarClientePJ(null, telaConsulta, 0);
            this.dispose();
        }else if (condcontajRadioButton.isSelected()){
            new FrmCadastrarClienteCondominio(null, telaConsulta, 0);
            this.dispose();
        }else if (pfchequejRadioButton.isSelected()){
            new FrmCadastrarClientePF(null, telaConsulta, 0);
            this.dispose();
        } else if (pjchequejRadioButton.isSelected()){
            new FrmCadastrarClientePJ(null, telaConsulta, 0);
            this.dispose();
        }else if (condchequejRadioButton.isSelected()){
            new FrmCadastrarClienteCondominio(null, telaConsulta, 0);
            this.dispose();
        }else if (entregaPFjRadioButton.isSelected()){
            new FrmCadastarClienteEntregaPF(null, telaConsulta, 0);
            this.dispose();
        } if (entregaPJjRadioButton.isSelected()){
            new FrmCadastarClienteEntregaPJ(null, telaConsulta, 0);
            this.dispose();
        }else JOptionPane.showMessageDialog(rootPane, "Selecione um Tipo de Cliente");
    }//GEN-LAST:event_salvarjButton2ActionPerformed

