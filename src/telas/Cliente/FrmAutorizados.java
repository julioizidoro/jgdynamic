    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.usuarioLogado.getAcesso().getClienteAutorizadoAdicionar() == 1) {
            Clienteautorizados autorizado = new Clienteautorizados();
            autorizado.setClienteIdcliente(this.cliente.getIdcliente());
            autorizado.setFoneCelular(foneCelularjFormattedTextField1.getText());
            autorizado.setNome(nomejTextField.getText());
            autorizado.setRg(rgjFormattedTextField.getText());
            ClienteController clienteController = new ClienteController();
            clienteController.salvarAutorizado(autorizado);
            nomejTextField.setText("");
            rgjFormattedTextField.setText("");
            foneCelularjFormattedTextField1.setText("");
            try {
                foneCelularjFormattedTextField1.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("(##)####-####")));
            } catch (ParseException ex) {
                Logger.getLogger(FrmAutorizados.class.getName()).log(Level.SEVERE, null, ex);
            }
            nomejTextField.requestFocus();
            setModelAutorizados();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Acesso Negado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (this.usuarioLogado.getAcesso().getClienteAutorizadoExcluir()==1){
            int linha = autorizadosjTable.getSelectedRow();
            if (linha>=0){
                ClienteController clienteController = new ClienteController();
                clienteController.excluirClienteAutorizado(listaAutorizados.get(linha));
                setModelAutorizados();
            }
        }else JOptionPane.showMessageDialog(rootPane, "Acesso Negado");
    }//GEN-LAST:event_jButton2ActionPerformed

