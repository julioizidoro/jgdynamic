    public void setModelAutorizados(){
        ClienteController clienteController = new  ClienteController();
        this.listaAutorizados = clienteController.listarAutorizados(this.cliente.getIdcliente());
        if (listaAutorizados==null){
            listaAutorizados  = new ArrayList<Clienteautorizados>();
        }
        
        modeAutorizados = new ConsultaAutorizadoTableModel(listaAutorizados);
        autorizadosjTable.setModel(modeAutorizados);
        autorizadosjTable.getColumnModel().getColumn(0).setPreferredWidth(200);
        autorizadosjTable.getColumnModel().getColumn(1).setPreferredWidth(30);
        autorizadosjTable.getColumnModel().getColumn(2).setPreferredWidth(30);
        autorizadosjTable.repaint();
    }

