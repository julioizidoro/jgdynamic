    public void carregarErro(BufferedReader leitor) throws IOException{
        boolean eof= true;
        while(!eof){
            String linha = leitor.readLine();
            if (linha!=null){
                errojTextArea.append(linha);
                eof = true;
            }else {
                eof = false;
            }
        }
    }
