    public String retirarPontos(String dado){
        String formatado ="";
        char c = ' ';
        for(int i=0;i<dado.length();i++){
            c = dado.charAt(i);
            if ((c!='.') && (c!=',') && (c!='-') && (c!='/') && (c!='(') && (c!=')')){
               formatado+= c;
            }
        }
        return formatado;
    }

    public String FormatarValoreMonetarios(String dado){
        String formatado ="";
        char c = ' ';
        for(int i=0;i<dado.length();i++){
            c = dado.charAt(i);
            if ((c!='.')){
               if (c==','){
                   formatado+= '.';
               } else {
                   formatado+= c;
               }
            }
        }
        return formatado;
    }


