    public void carregarGrupoProduto(){
        GrupoProdutoController grupoProdutoController = new GrupoProdutoController();
        List<Grupoproduto> listaGrupoprodutos = grupoProdutoController.listarGrupoProduto();
        if (listaGrupoprodutos!=null){
            for(int i=0;i<listaGrupoprodutos.size();i++){
                grupojComboBox.addItem(listaGrupoprodutos.get(i));
            }
        }
    }
    
    public void carregarSubGrupoProduto(int idGrupo){
        GrupoProdutoController grupoProdutoController = new GrupoProdutoController();
        List<Subgrupoproduto> listaSubGrupo = grupoProdutoController.listSubGrupoProduto(idGrupo);
        if (listaSubGrupo!=null){
            for(int i=0;i<listaSubGrupo.size();i++){
                subgrupojComboBox.addItem(listaSubGrupo.get(i));
            }
        }
    }
