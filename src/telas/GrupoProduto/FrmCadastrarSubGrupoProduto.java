    private void salvarTelajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarTelajButtonActionPerformed
        if (subGrupoProduto==null){
            subGrupoProduto = new Subgrupoproduto();
            subGrupoProduto.setGrupoproduto(grupoProduto.getIdgrupoProduto());
        }
        subGrupoProduto.setDescricao(descricaojTextField.getText());
        if (comissaojTextField.getText().length()==0){
            subGrupoProduto.setPercentualComissao(00.);
        }else subGrupoProduto.setPercentualComissao(Formatacao.ConvercaoMonetariaDouble(comissaojTextField.getText()));
        GrupoProdutoController grupoProdutoController = new GrupoProdutoController();
        grupoProdutoController.salvarSubGrupoProduto(subGrupoProduto);
        this.dispose();
    }//GEN-LAST:event_salvarTelajButtonActionPerformed

