    private void SalvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarjButtonActionPerformed
        configura.setEmailOrigem(emailjTextField.getText());
        configura.setEmailSmtp(smtpjTextField.getText());
        String senha="";
        char[] email = emailSenhajPasswordField.getPassword();
        for (int i=0;i<email.length;i++){
            senha+=email[i];
        }
        configura.setEmailSenha(senha);
        configura.setBdlocal(dbLocaljTextField1.getText());
        configura.setBdporta(dbPortajTextField.getText());
        configura.setBdNome(dbNomejTextField.getText());
        configura.setBdusuario(dbUsuariojTextField.getText());
        senha="";
        char[] db = dbSenhaSenhajPasswordField1.getPassword();
        for (int i=0;i<db.length;i++){
            senha+=db[i];
        }
        configura.setBdSenha(senha);
        
        //Conex√£o remota
        
        configura.setRemotoNome(remotaNomejTextField.getText());
        configura.setRemotolocal(remotaLocaljTextField.getText());
        configura.setRemotoporta(remotaPortajTextField.getText());
        configura.setRemotousuario(remotaUsuariojTextField.getText());
        senha="";
        db = remotaSenhajPasswordField.getPassword();
        for (int i=0;i<db.length;i++){
            senha+=db[i];
        }
        configura.setRemotoSenha(senha);
        
        
        
        Sigap utilitario = new Sigap();
        try{
            utilitario.serelizarConfiguracao(configura);
            JOptionPane.showMessageDialog(null,"Arquivo Gravado com Sucesso");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro ao Gravar\n" + ex);

        }
        this.dispose();

}//GEN-LAST:event_SalvarjButtonActionPerformed

