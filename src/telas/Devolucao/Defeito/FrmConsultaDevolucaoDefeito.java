    public void consultarFornecedor(Fornecedor fornecedor) {
        if (fornecedor != null) {
            this.fornecedor = fornecedor;
            fornecedorjTextField.setText(fornecedor.getNomeFantasia());
            FornecedorEnviojTextField.setText(fornecedor.getNomeFantasia());
            carregarModelDevolucao();
        }
    }

    public void carregarModelDevolucao() {
        if (this.fornecedor != null) {
            DevolucaoController devolucaoController = new DevolucaoController();
            String sql = "Select d FROM Viewdevolucaodefeitoproduto d where d.empresaIdempresa=" + config.getEmpresa().getIdempresa()
                    + " and d.fornecedorIdfornecedor=" + fornecedor.getIdfornecedor() + " and d.numeroEnvio=0";

            listaDevolucao = devolucaoController.listarDevolucaoDefeitoProduto(sql);
            if (listaDevolucao == null) {
                listaDevolucao = new ArrayList<Viewdevolucaodefeitoproduto>();
            }
        } else {
            listaDevolucao = new ArrayList<Viewdevolucaodefeitoproduto>();
        }
        DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
        renderer.setHorizontalAlignment(SwingConstants.RIGHT);
        modelDevolucao = new DevolucaoDefeitoTableModel(listaDevolucao);
        devolucaojTable.setModel(modelDevolucao);
        devolucaojTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        devolucaojTable.getColumnModel().getColumn(1).setPreferredWidth(10);
        devolucaojTable.getColumnModel().getColumn(2).setPreferredWidth(100);
        devolucaojTable.getColumnModel().getColumn(3).setPreferredWidth(30);
        devolucaojTable.getColumnModel().getColumn(4).setCellRenderer(renderer);
        devolucaojTable.getColumnModel().getColumn(4).setPreferredWidth(50);
        devolucaojTable.getColumnModel().getColumn(5).setPreferredWidth(50);
        devolucaojTable.repaint();
        calcularValorTotalDevolucao();
    }

