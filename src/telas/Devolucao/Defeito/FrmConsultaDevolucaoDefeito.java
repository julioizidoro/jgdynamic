    public void calcularValorTotalDevolucao() {
        if (listaDevolucao == null) {
            totalDevolucaojLabel.setText("0,00");
        } else {
            float valor = 0;
            for (int i = 0; i < listaDevolucao.size(); i++) {
                valor = valor + listaDevolucao.get(i).getValorProduto();
            }
            totalDevolucaojLabel.setText(Formatacao.foramtarFloatString(valor));
        }
    }

    public void calcularValorTotalEnvio() {
        if (listaEnvio == null) {
            totalEnviojLabel.setText("0,00");
        } else {
            float valor = 0;
            for (int i = 0; i < listaEnvio.size(); i++) {
                valor = valor + listaEnvio.get(i).getValor();
            }
            totalEnviojLabel.setText(Formatacao.foramtarFloatString(valor));
        }
    }

    public void incluirProdutoparaEnvio(int linha) {
        EnvioDevolucaoDefeitoBean envio = new EnvioDevolucaoDefeitoBean();
        envio.setDescricao(listaDevolucao.get(linha).getDescricao());
        envio.setFornecedor(fornecedor);
        envio.setIdDevolucaoDefeito(listaDevolucao.get(linha).getIddevolucaodefeito());
        envio.setIdProduto(listaDevolucao.get(linha).getProdutoIdproduto());
        envio.setReferencia(listaDevolucao.get(linha).getReferencia());
        envio.setValor(listaDevolucao.get(linha).getValorProduto());
        listaEnvio.add(envio);
        carregarModelEnvio();
    }

    public void excluirProdutoEnvio(int linha) {
        if (listaEnvio != null) {
            int idDevolucao = listaDevolucao.get(linha).getIddevolucaodefeito();
            for (int i = 0; i < listaEnvio.size(); i++) {
                int idEnvio = listaEnvio.get(i).getIdDevolucaoDefeito();
                if (idDevolucao == idEnvio) {
                    listaEnvio.remove(i);
                    JOptionPane.showMessageDialog(rootPane, "Produto Removido do Envio");
                }
            }
        }
    }

    public void salvarDevolucaoDefeitoEnvio() {
        DevolucaoController devolucaoController = new DevolucaoController();
        Enviodevolucaodefeito envio = new Enviodevolucaodefeito();
        if (listaDevolucao.size() > 0) {
            envio.setData(new Date());
            envio.setFornecedor(fornecedor.getIdfornecedor());
            envio.setUsuarios(usuarioLogado.getUsuario().getIdusuarios());
            envio.setVededor(vendedorjTextField.getText());
            envio.setEmpresa(this.config.getEmpresa().getIdempresa());
            envio.setRecebida("Não");
            envio = devolucaoController.salvarEnvioDevolucao(envio);
        }
        for (int i = 0; i < listaEnvio.size(); i++) {
            Enviodevolucaodefeitoproduto envioProduto = new Enviodevolucaodefeitoproduto();
            envioProduto.setDevolucaoDefeito(listaDevolucao.get(i).getIddevolucaodefeito());
            envioProduto.setEnvioDevolucaoDefeito(envio.getIdenvioDevolucaoDefeito());
            envioProduto.setEmpresa(this.config.getEmpresa().getIdempresa());
            devolucaoController.salvarDevolucaoDefeitoProduto(envioProduto);
            if (envio != null) {
                devolucaoController.salvarDevolucaoDefetio(listaDevolucao.get(i).getIddevolucaodefeito(), envio.getIdenvioDevolucaoDefeito());
            }
        }
        listaEnvio = new ArrayList<EnvioDevolucaoDefeitoBean>();
        carregarModelDevolucao();
        carregarModelEnvio();
    }

