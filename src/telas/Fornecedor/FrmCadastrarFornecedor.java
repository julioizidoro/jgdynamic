    private void preenherCampoCadastro(){
        razaoSocialjTextField.setText(fornecedor.getRazaoSocial());
        nomeFantasiajTextField.setText(fornecedor.getNomeFantasia());
        cnpjjFormattedTextField.setText(fornecedor.getCnpj());
        iEjTextField.setText(fornecedor.getIe());
        foneComercialjFormattedTextField.setText(fornecedor.getTelefone());
        foneCelularjFormattedTextField.setText(fornecedor.getCelular());
        foneEscritoriojFormattedTextField.setText(fornecedor.getEscritorio());
        foneFaxjFormattedTextField.setText(fornecedor.getFax());
        enderecojTextField.setText(fornecedor.getEndereco());
        complementojTextField.setText(fornecedor.getComplemento());
        bairrojTextField.setText(fornecedor.getBairro());
        cepjFormattedTextField.setText(fornecedor.getCep());
        if (fornecedor.getMunicipios() == 0) {
            cidadejTextField.setText(fornecedor.getCidade());
            estadojTextField.setText(fornecedor.getUf());
        } else {
            MunicipiosController municipiosController = new MunicipiosController();
            this.municipios = municipiosController.getMunicipio(fornecedor.getMunicipios());
            if (municipios != null) {
                cidadejTextField.setText(municipios.getNomeMunicipio());
                estadojTextField.setText(municipios.getEstado());
            }
        }
        emailjTextField.setText(fornecedor.getEmail());
        vendedorjTextField.setText(fornecedor.getVendedor());
        contatojTextField.setText(fornecedor.getContato());
        prazoEntregajTextField.setText(Formatacao.formatarIntegerString(fornecedor.getPrazoEntrega()));
        descontojFormattedTextField.setText(Formatacao.formatarLogString(fornecedor.getDesconto()));
        parcelasjTextField.setText(Formatacao.formatarIntegerString(fornecedor.getParcela()));
        prazoPagtojTextField.setText(fornecedor.getPrazo());
        formaPagtojTextField.setText(fornecedor.getFormaPagamento());
        fretejTextField.setText(fornecedor.getFrete());
    }

