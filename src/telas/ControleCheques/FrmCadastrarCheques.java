    private void preecherCliente(){
        ClienteFacade clienteFacade = new ClienteFacade();
        //Dados do Cliente
        nomejTextField.setText(cliente.getNome());
        nomejTextField.setEditable(false);
        try {
            //dados do endereco
            
            clienteEndereco = clienteFacade.consultarClienteEndereco(cliente.getIdcliente());
            if (clienteEndereco==null){
                JOptionPane.showMessageDialog(rootPane, "EnderÃ§o do Cliente nÃ£o Localizado");
            }else {
                tipoLogradourojTextField.setText(clienteEndereco.getTipoLogradouro());
                tipoLogradourojTextField.setEditable(false);
                logradourojTextField.setText(clienteEndereco.getLogradouro());
                logradourojTextField.setEditable(false);
                numerojTextField.setText(clienteEndereco.getNumero());
                numerojTextField.setEditable(false);
                complementojTextField.setText(clienteEndereco.getComplemento());
                complementojTextField.setEditable(false);
                bairrojTextField.setText(clienteEndereco.getBairro());
                bairrojTextField.setEditable(false);
                cepjFormattedTextField.setText(clienteEndereco.getCep());
                cepjFormattedTextField.setEditable(false);
                cidadejTextField.setText(clienteEndereco.getCidade());
                cidadejTextField.setEditable(false);
                estadojComboBox.setSelectedItem(clienteEndereco.getEstado());
                estadojComboBox.setEditable(false);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmCadastrarCheques.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Erro Consultar endereco Cliente " + ex);
        }
    }
    
