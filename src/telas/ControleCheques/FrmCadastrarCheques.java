    private boolean validarCampos(){
        cliente.setDocFederal(cpfjFormattedTextField.getText());
        if (cliente.getDocFederal().equalsIgnoreCase(null)){
            JOptionPane.showMessageDialog(rootPane, "Erro Cliente nÃ£o Localizado");
            return false;
        }
        if (numeroBancojTextField.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Numero do Banco nÃ£o preechido");
            return false;
        }
        if (agenciajTextField.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Agencia nÃ£o preenchida");
            return false;
        }
        if (contaCorrentejTextField.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Conta Corrente nÃ£o preenchida");
            return false;
        }
        if (numeroChequejTextField.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Nunero do Cheque nÃ£o preenchido");
            return false;
        }
        if (valorChequejTextField.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Valor do Cheque nÃ£o preenchido");
            return false;
        }
        if (dataEmissaojDateChooser.getDate()==null){
            JOptionPane.showMessageDialog(rootPane, "Data EmissÃ£o invÃ¡lida");
            return false;
        }
        if (dataCompensacaojDateChooser.getDate()==null){
            JOptionPane.showMessageDialog(rootPane, "Data de CompensaÃ§Ã£o invÃ¡lida");
            return false;
        }
        return true;
    }
    
    private void salvarDados(){
        ClienteController clienteController = new ClienteController();
        if (cliente.getIdcliente()==null){
            cliente.setDocFederal(cpfjFormattedTextField.getText());
            cliente.setTipo(tipoClientejComboBox.getSelectedItem().toString());
            cliente.setNome(nomejTextField.getText());
            cliente.setSituacao("ATIVO");
            cliente.setOperacao(2);
            cliente = clienteController.salvarCliente(cliente);
            clientefechamento = new Clientefechamento();
            clientefechamento.setClienteIdcliente(cliente.getIdcliente());
            clientefechamento.setDiaPagamento(1);
            clientefechamento.setFormaPagamento("Cheque");
            clientefechamento.setLimiteCredito(0.0);
            clientefechamento.setNotaFiscal("NAO");
            clientefechamento.setTipoFechamento("Diario");
            clienteController.salvarFechamento(clientefechamento);
        }
        if (clienteEndereco.getIdclienteEndereco()==null){
            clienteEndereco.setTipoLogradouro(tipoLogradourojTextField.getText());
            clienteEndereco.setLogradouro(logradourojTextField.getText());
            clienteEndereco.setNumero(numerojTextField.getText());
            clienteEndereco.setComplemento(complementojTextField.getText());
            clienteEndereco.setBairro(bairrojTextField.getText());
            clienteEndereco.setCep(cepjFormattedTextField.getText());
            clienteEndereco.setCidade(cidadejTextField.getText());
            clienteEndereco.setEstado(estadojComboBox.getSelectedItem().toString());
            clienteEndereco.setClienteIdcliente(cliente.getIdcliente());
            clienteController.salvarEndereco(clienteEndereco);
            
        }
        ControleChequeController controleChequeController = new ControleChequeController();
        cheque.setAgencia(agenciajTextField.getText());
        cheque.setBanco(numeroBancojTextField.getText());
        cheque.setChequeTroca(trocajComboBox.getSelectedItem().toString());
        cheque.setCliente(cliente.getIdcliente());
        cheque.setContaCorrente(contaCorrentejTextField.getText());
        cheque.setDataCompensacao(dataCompensacaojDateChooser.getDate());
        cheque.setDataEmissao(dataEmissaojDateChooser.getDate());
        cheque.setNumeroCheque(numeroChequejTextField.getText());
        cheque.setPreDatado(preDatadojComboBox.getSelectedItem().toString());
        cheque.setSituacao(situacaojTextField.getText());
        cheque.setValorCheque(Formatacao.ConvercaoMonetariaDouble(valorChequejTextField.getText()));
        cheque.setEmpresa(config.getEmpresa().getIdempresa());
        controleChequeController.salvarCehque(cheque);
        this.telaCheque.carregarModelCheque();
        this.dispose();
    }
