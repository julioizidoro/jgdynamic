    public  void carregarModel(int idEmpresa){
        EmissorecfController emissorecfController = new EmissorecfController();
        listaECF = emissorecfController.listar(idEmpresa);
        if (listaECF==null){
            listaECF = new ArrayList<Emissorecf>();
        }
        model = new EmissorECFTableModel(listaECF);
        ecfjTable.setModel(model);
        ecfjTable.getColumnModel().getColumn(0).setPreferredWidth(20);
        ecfjTable.getColumnModel().getColumn(1).setPreferredWidth(150);
        ecfjTable.getColumnModel().getColumn(2).setPreferredWidth(150);
        ecfjTable.getColumnModel().getColumn(3).setPreferredWidth(80);
        ecfjTable.repaint();
    }

