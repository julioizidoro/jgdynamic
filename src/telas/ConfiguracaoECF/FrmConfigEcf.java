    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        int linha = ecfjTable.getSelectedRow();
        if (linha>=0){
            String data = JOptionPane.showInputDialog("Infome data no padrão DD/MM/AAAA");
            Date dataZ = Formatacao.ConvercaoStringData(data);
            Emissorecf emissor = listaECF.get(linha);
            emissor.setDataReducaoZ(dataZ);
            EmissorecfController emissorecfController = new EmissorecfController();
            emissorecfController.salvar(emissor);
            carregarModel(emissor.getEmpresa());
        }
        
        
    }//GEN-LAST:event_salvarjButtonActionPerformed

