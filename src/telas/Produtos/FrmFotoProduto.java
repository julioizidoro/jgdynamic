public byte[] gerarFoto(File f, FileInputStream fio){
        int tamanho = (int) f.length();
        byte[] byteArray = new byte[tamanho];
        BufferedInputStream  bis = new BufferedInputStream(fio);
        boolean done = false;
        int bytesread = 0;
        do {
            int b = 0;
            try {
                b = bis.read();
            } catch (IOException ex) {
                Logger.getLogger(FrmFotoProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (b == -1) {
                done = true;
                break;
            } else {
                byteArray[bytesread++] = (byte) b;
            }
         } while (!done);
         return byteArray;
    }

    public void localizarFotoProduto(){
        ProdutoController produtoController = new ProdutoController();
        produtoImagem = produtoController.consultarProdutoFoto(produto.getIdProduto());
        if (produtoImagem!=null){
            byte[] byteArray = produtoImagem.getImagem();
            fotojLabel.setIcon(new ImageIcon(byteArray));
            fotojLabel.setText(null);
            fotojLabel.repaint();
        }else {
            fotojLabel.setText("Produto n√£o possui Foto");
            produtoImagem = new ProdutoImagem();
        }
    }

