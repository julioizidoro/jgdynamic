    private void novoValorVendajTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_novoValorVendajTextFieldKeyTyped
        if (evt.getKeyChar()=='.'){
            evt.setKeyChar(',');
        }
        String caracteres = "0987654321,-";
        if (evt.getKeyChar() != '\b') {
            if (!caracteres.contains(evt.getKeyChar() + "")) {
                evt.consume();
            }
        }
    }//GEN-LAST:event_novoValorVendajTextFieldKeyTyped

    private void salvarjButton2SalvarCadastroAssociado(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButton2SalvarCadastroAssociado
        if (novoValorVendajTextField.getText().length() > 0) {
            EstoqueController estoqueController = new EstoqueController();
            Float valor = Formatacao.formatarStringfloat(novoValorVendajTextField.getText());
            estoque.setValorVenda(valor);
            estoque = estoqueController.salvarEstoque(estoque);
            telaConsulta.setModelProduto(null, estoque);
            this.dispose();
        }else JOptionPane.showMessageDialog(rootPane, "Preencha nova valor de venda");
    }//GEN-LAST:event_salvarjButton2SalvarCadastroAssociado

