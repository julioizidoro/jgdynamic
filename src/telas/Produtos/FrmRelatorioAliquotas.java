    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql = "SELECT * FROM viewrelatorioaliquota where ";
        boolean marcacao=false;
        if (this.aliquota!=null){
            sql = sql + " idaliquota=" + aliquota.getIdaliquota();
            marcacao=true;
        }
        if (grupoProduto!=null){
            if (marcacao){
                sql = sql + " and ";
            }
            sql = sql + " idgrupoproduto=" + grupoProduto.getIdgrupoProduto();
        }
        if (fornecedor!=null){
            if (marcacao){
                sql = sql + " and ";
            }
            sql = sql + " idfornecedor=" + fornecedor.getIdfornecedor();
        }
        sql = sql + " order by descricao";
        String url = ("telas/Produtos/reportAliquota.jasper");
        Map parameters = new HashMap();
        try {
            parameters.put("nomeFantasia", this.config.getEmpresa().getNomeFantasia());
            parameters.put("RazaoSocial", this.config.getEmpresa().getRazaoSocial());
            parameters.put("sql", sql);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "N√£o foi poss√≠vel gerar o relat√≥rio " + ex);
            ex.printStackTrace();
        }
        new relatoriosJasper(url, parameters);
    }//GEN-LAST:event_jButton1ActionPerformed

