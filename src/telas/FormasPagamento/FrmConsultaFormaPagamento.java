    private void formaPagamentojTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formaPagamentojTableMouseClicked
        int linha = formaPagamentojTable.getSelectedRow();
        if (linha>=0){
            EditarjButton.setEnabled(true);
            ExcluirjButton.setEnabled(true);
        }
    }//GEN-LAST:event_formaPagamentojTableMouseClicked

    private void EditarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarjButtonActionPerformed
        int linha = formaPagamentojTable.getSelectedRow();
        if (linha>=0){
            new FrmCadastrarFormaPagamento(listaFormaPagamento.get(linha), this, this.config.getEmpresa().getIdempresa());
        }
    }//GEN-LAST:event_EditarjButtonActionPerformed

    private void ExcluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirjButtonActionPerformed
        int linha = formaPagamentojTable.getSelectedRow();
        if (linha>=0){
            FormaPagamentoController formaPagamentoController = new FormaPagamentoController();
            formaPagamentoController.excluir(listaFormaPagamento.get(linha));
            carregarListaFormaPagamento();
        }
    }//GEN-LAST:event_ExcluirjButtonActionPerformed

