    public void carregarListaFormaPagamento(){
        FormaPagamentoController formaPagamentoController = new  FormaPagamentoController();
        listaFormaPagamento = formaPagamentoController.listarFormaPagamento(config.getEmpresa().getIdempresa());
        if (listaFormaPagamento!=null){
            model = new ConsultaFormaPagamentoTableModel(listaFormaPagamento);
        }else model = new ConsultaFormaPagamentoTableModel(new ArrayList<Formapagamento>());
        formaPagamentojTable.setModel(model);
        formaPagamentojTable.getColumnModel().getColumn(0).setPreferredWidth(20);
        formaPagamentojTable.getColumnModel().getColumn(1).setPreferredWidth(200);
        formaPagamentojTable.getColumnModel().getColumn(2).setPreferredWidth(20);
        formaPagamentojTable.getColumnModel().getColumn(3).setPreferredWidth(50);
        formaPagamentojTable.repaint();
    }

