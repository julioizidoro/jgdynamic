    private void salvarjButton2SalvarCadastroAssociado(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButton2SalvarCadastroAssociado
        FormaPagamentoController formaPagamentoController = new FormaPagamentoController();
        formaPagamento.setDescricao(descricaojTextField.getText());
        formaPagamento.setLocalRegistro(localjComboBox.getSelectedItem().toString());
        if (formaPagamento.getLocalRegistro().equalsIgnoreCase("Banco")){
        Banco banco = (Banco) bancojComboBox.getSelectedItem();
        formaPagamento.setBanco(banco.getIdbanco());
        BandeirasCartao bandeira = (BandeirasCartao) bandeirajComboBox.getSelectedItem();
        formaPagamento.setBandeiraCartao(bandeira.getIdbandeirasCartao());
        formaPagamento.setComissaoVista(Double.parseDouble(comissaoDebitojTextField.getText()));
        formaPagamento.setComissaoParcelado(Double.parseDouble(comissaoParceladojTextField.getText()));
        formaPagamento.setNumeroParcelas(Integer.parseInt(numeroParcelasjTextField.getText()));
        formaPagamento.setPrazo(Integer.parseInt(prazoCreditojTextField.getText()));
        }else {
            formaPagamento.setBandeiraCartao(1);
            formaPagamento.setComissaoVista(0.0);
            formaPagamento.setComissaoParcelado(0.0);
            formaPagamento.setNumeroParcelas(0);
            formaPagamento.setPrazo(0);
        }
        formaPagamento.setEmpresa(idEmpresa);
        formaPagamentoController.salvarFormapagamento(formaPagamento);
        telaCosulta.carregarListaFormaPagamento();
        this.dispose();
    }//GEN-LAST:event_salvarjButton2SalvarCadastroAssociado

