    public void preencherAlteracao(){
        if (formaPagamento.getLocalRegistro().equalsIgnoreCase("Banco")){
            preencherBandeiraCartao();
            preencherBanco();
            bancojComboBox.setEnabled(true);
            bandeirajComboBox.setEnabled(true);
            comissaoDebitojTextField.setEnabled(true);
            comissaoParceladojTextField.setEnabled(true);
            numeroParcelasjTextField.setEnabled(true);
            prazoCreditojTextField.setEnabled(true);
            BandeiraCartaoController bandeiraCartaoController = new BandeiraCartaoController();
            BandeirasCartao bandeira = bandeiraCartaoController.pesquisarid(formaPagamento.getBandeiraCartao());
            if (bandeira!=null){
                bancojComboBox.setSelectedItem(bandeira);
            }
            comissaoDebitojTextField.setText(Formatacao.foramtarDoubleString(formaPagamento.getComissaoVista()));
            comissaoParceladojTextField.setText(Formatacao.foramtarDoubleString(formaPagamento.getComissaoParcelado()));
            numeroParcelasjTextField.setText(String.valueOf(formaPagamento.getNumeroParcelas()));
            prazoCreditojTextField.setText(String.valueOf(formaPagamento.getPrazo()));
        }else {
            bandeirajComboBox.setEditable(false);
            bancojComboBox.setEnabled(false);
            comissaoDebitojTextField.setEnabled(false);
            comissaoParceladojTextField.setEnabled(false);
            numeroParcelasjTextField.setEnabled(false);
            prazoCreditojTextField.setEnabled(false);
        }
        descricaojTextField.requestFocus();
    }
    
    public void preencherBandeiraCartao(){
        BandeiraCartaoController bandeiraCartaoController = new BandeiraCartaoController();
        bancojComboBox = new JComboBox();
        List<BandeirasCartao> listaBandeiraCarta = bandeiraCartaoController.listar();
        if (listaBandeiraCarta!=null){
            for(int i=0;i<listaBandeiraCarta.size();i++){
                bancojComboBox.addItem(listaBandeiraCarta.get(i));
            }
        }
    }
    
    public void preencherBanco(){
        BancoController bancoController = new BancoController();
        List<Banco> listaBanco = bancoController.listar();
        if(listaBanco!=null){
            for(int i=0;i<listaBanco.size();i++){
                bancojComboBox.addItem(listaBanco.get(i));
            }
        }
    }
    
