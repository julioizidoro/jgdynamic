    public void carregarListaProduto(int idContasReceber) {
        int idVenda = 0;
        ContasReceberController contasReceberController = new ContasReceberController();
        Contasreceberprodutos contasreceberprodutos = new Contasreceberprodutos();
        contasreceberprodutos = contasReceberController.consultarContasReceberProduto(idContasReceber);
        if (contasreceberprodutos != null) {
            idVenda = contasreceberprodutos.getVenda().getIdvenda();

            VendaController vendaController = new VendaController();
            this.venda = vendaController.getVenda(idVenda);
            if (venda != null) {
                listaProdutos = vendaController.listarSaidaVenda(idVenda);
            }

            if (listaProdutos == null) {
                listaProdutos = new ArrayList<Saida>();
            }

            modelProdutos = new ConsultaProdutosContaReceberTableModel(listaProdutos);
            dataVendajTextField.setText(Formatacao.ConvercaoDataPadrao(venda.getDataVenda()));
            itensjTextField.setText(String.valueOf(venda.getNumeroItem()));
            valorVendajTextField.setText(Formatacao.foramtarFloatString(venda.getValorVenda()));

            DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
            renderer.setHorizontalAlignment(SwingConstants.RIGHT);

            produtosjTable.setModel(modelProdutos);
            produtosjTable.getColumnModel().getColumn(0).setPreferredWidth(20);
            produtosjTable.getColumnModel().getColumn(1).setPreferredWidth(300);
            produtosjTable.getColumnModel().getColumn(2).setPreferredWidth(40);
            produtosjTable.getColumnModel().getColumn(3).setPreferredWidth(40);
            produtosjTable.getColumnModel().getColumn(4).setCellRenderer(renderer);
            produtosjTable.getColumnModel().getColumn(4).setPreferredWidth(40);
            produtosjTable.repaint();
        }else {
            JOptionPane.showMessageDialog(rootPane, "Não existem prodiutos para esta Conta");
            this.dispose();
        }
    }
