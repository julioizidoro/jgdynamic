    public void pesquisarFechamentoMes(){
        FechamentoContasReceberController fechamentoContasReceberController = new FechamentoContasReceberController();
        mes = fechamentoContasReceberController.consultaFechamentoMes(mesjTextField.getText());
        if (mes!=null){
            listaFechamento = fechamentoContasReceberController.consultaFechamentoCliente(mes.getIdfechamentoMes());
            if (listaFechamento==null){
                listaFechamento = new ArrayList<ViewClienteFechamento>();
            }
            gerarFechamentoModel();
            calcularValotesTotais();
        }
    }
    
    
