    public void calcularValotesTotais(){
        if (listaFechamento!=null){
            double valorPagar=0;
            double valorConta=0;
            double valorCredito=0;
            for(int i=0;i<listaFechamento.size();i++){
                valorConta+= listaFechamento.get(i).getValorConta();
                valorCredito+= listaFechamento.get(i).getValorCredito();
                valorPagar+= listaFechamento.get(i).getValorPagar();
            }
            valorContajTextField.setText(Formatacao.foramtarDoubleString(valorConta));
            valorCreditosjTextField.setText(Formatacao.foramtarDoubleString(valorCredito));
            valorReceberjTextField.setText(Formatacao.foramtarDoubleString(valorPagar));
        }
        
    }
    
    public void exportaFechamentoClienteExcel(){
        FechamentoContasReceberController fechamentoContasReceberController = new FechamentoContasReceberController();
        Sigap utilitario = new Sigap();
        ConfiguracaoSistema configura = new ConfiguracaoSistema();
        configura = (ConfiguracaoSistema) utilitario.deserializarConfiguracao();
        Map mapa = new HashMap();
        String banco = "use " + configura.getBdNome();
        String sql = "Select * from viewClienteFechamento where fechamentomes_idfechamentomes=" + mes.getIdfechamentoMes() + " order by nome";
        String linhaBanco = "jdbc:mysql://" + configura.getBdlocal()+ ":" + configura.getBdporta() + "/" +
                configura.getBdNome();
        fechamentoContasReceberController.exportarFechamentoClinte("Fechamento Cliente", linhaBanco, banco, sql, configura.getBdusuario(), configura.getBdSenha());
    }


