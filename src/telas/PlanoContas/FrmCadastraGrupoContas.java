        this.grupoContas.setNumeroConta(numeroGrupojTextField.getText());
        this.grupoContas.setNumeroSubGrupo(0);
        try {
            GrupoContaFacade facade = new GrupoContaFacade();
            facade.salvarGrupoconta(grupoContas);
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Dados não foram salvos" + ex.getMessage());
        }
